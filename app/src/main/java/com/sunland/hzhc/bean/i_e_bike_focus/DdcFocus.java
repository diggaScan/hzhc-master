package com.sunland.hzhc.bean.i_e_bike_focus;

public class DdcFocus {
    private String sj;//		发生时间
    private String dw;//		录入单位
    private String lb;//		类别
    private String nr;//		关注内容
    private String bh;//		编号
    private String status;//		状态：0/无数据1/有数据

    public String getSj() {
        return sj;
    }

    public void setSj(String sj) {
        this.sj = sj;
    }

    public String getDw() {
        return dw;
    }

    public void setDw(String dw) {
        this.dw = dw;
    }

    public String getLb() {
        return lb;
    }

    public void setLb(String lb) {
        this.lb = lb;
    }

    public String getNr() {
        return nr;
    }

    public void setNr(String nr) {
        this.nr = nr;
    }

    public String getBh() {
        return bh;
    }

    public void setBh(String bh) {
        this.bh = bh;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
